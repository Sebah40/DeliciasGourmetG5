    private void btnCancelarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarReservaActionPerformed
        // TODO add your handling code here:
        int selectedIndex = listReservasCliente.getSelectedIndex();

        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una reserva para cancelar.");
            return;
        }

        List<Reserva> reservas = reservaService.getReservasFromCliente(SessionManager.getInstance().getCurrentCliente().getClienteId());

        if (selectedIndex >= reservas.size()) {
            JOptionPane.showMessageDialog(this, "La reserva seleccionada no est√° disponible.");
            return;
        }

        Reserva selectedReserva = reservas.get(selectedIndex);

        try {
            reservaService.cancelarReserva(selectedReserva.getIdReserva());

            JOptionPane.showMessageDialog(this, "Reserva cancelada exitosamente.");

            refreshReservas();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cancelar la reserva: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCancelarReservaActionPerformed

